#include "push_swp.h"

void	ft_index(t_v *v)
{
	v->i = -1;
	while (++v->i < v->d)
	{
		v->n = -1;
		v->ct = 0;
		while (++v->n < v->d)
		{
			if (v->tab[0][v->i] > v->tab[0][v->n])
				v->ct++;
		}
		v->tab[1][v->i] = v->ct;
	}
	v->i = -1;
	while (++v->i <= v->d - 1)
		v->tab[0][v->i] = v->tab[1][v->i];
}
